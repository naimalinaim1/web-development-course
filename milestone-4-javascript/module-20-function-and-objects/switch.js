var color = 'yellow';

switch (color) {
    case 'green':
        console.log('Your are a green friend');
        break;

    case 'red':
        console.log('Your are a red friend');
        break;

    case 'yellow':
        console.log('Your are a yellow friend');
        break;

    default:
        console.log('Your are a white white friend');
        break;
}